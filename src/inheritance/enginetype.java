package inheritance;

public class enginetype 
{
	 private String type;
	    private int horsepower;

	    public enginetype(String type,int horsepower)
	    {
	        this.type = type;
	        this.horsepower = horsepower;
	    }

	    // Getter methods
	    public String getType() 
	    {
	        return type;
	    }

	    public int getHorsepower() 
	    {
	        return horsepower;
	    }
}
